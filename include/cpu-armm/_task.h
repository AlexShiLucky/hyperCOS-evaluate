#ifndef _TASK0418
#define _TASK0418

static inline void __task_switch_sync(task_t * tn)
{
	_task_pend = tn;
	irq_unlock();
	cpu_req_switch();
	while (_task_pend) ;
	irq_lock();
}

#define _task_switch_sync(_tn, _or)	__task_switch_sync(_tn)

static inline void task_load(task_t * t)
{
	_task_cur = t;
	__task_switch_sync(t);
}

#endif
